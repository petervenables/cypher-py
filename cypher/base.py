class BaseCypher:
    """
    BaseCypher - a do-nothing class from which to base other cypher methods 
    """

    def encode(in_str):
        """ encodes a string using no method at all.
        
        Arguments:
          in_str (string): a string to encode

        Returns:
          string: the same string provided

        """
        return in_str

    def decode(in_str):
        """ decodes a string using no method at all.
        
        Arguments:
          in_str (string): a string to decode

        Returns:
          string: the same string provided
          
        """
        return in_str

